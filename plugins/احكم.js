export default {
    name: "احكم",
    aliases: ["حكم"],
    category: "fun",
    async run({ sock, m, text }) {
        const chatId = m.key.remoteJid;

        try {
            // تحديد الضحية عن طريق الرد (Reply) أو المنشن
            const quoted = m.message?.extendedTextMessage?.contextInfo;
            const victim = quoted?.participant || m.message?.extendedTextMessage?.contextInfo?.mentionedJid?.[0];

            // إذا لم يتم تحديد ضحية، يطلب البوت منشن أو رد
            if (!victim) {
                return await sock.sendMessage(chatId, { 
                    text: "｢ ⚠️ ｣ *يـجـب الـرد عـلـى رسـالـة الـعـضـو أو مـنـشـنـه [.احكم @منشن] لـتـنـفـيـذ الـحـكـم!*" 
                }, { quoted: m });
            }

            const judgments = [
                "تغيير صورة البروفايل لصورة مضحكة لمدة 24 ساعة.",
                "إرسال رسالة صوتية وأنت تغني 'أنا سمكة'.",
                "كتابة 'بوت سولو و سونغ اعمامي' 10 مرات في الشات.",
                "ممنوع من الكلام لمدة 10 دقائق.",
                "عليك الرد على أي رسالة قادمة بكلمة 'باع' أو 'وداع' لمدة 5 دقائق.",
                "أرسل نكتة مضحكة الآن (اذا كانت غير مضحكة يصبح اسمك 'كرومبو'.",
                "قم بعمل منشن لأكثر شخص تحبه في الجروب وقل له 'أنت قدوتي'.",
                "غير اسمك في الجروب إلى 'أنا من عشاق سولو' لمدة ساعة.",
                "أرسل صورة لآخر صورة التقطتها في هاتفك.",
                "تكلم باللغة الفصحى فقط لمدة 15 دقيقة.",
                "قم بعمل منشن لمشرف وقل له 'أنت أفضل مشرف رأيته في حياتي'.",
                "أرسل رسالة صوتية تقلد فيها صوت حيوان من اختيارك.",
                "اكتب 'سأصبح مطيعاً من الآن' في الشات.",
                "قم بوصف نفسك بـ 3 كلمات مضحكة.",
                "اختر عضواً وقول له نصيحة غريبة.",
                "أرسل ملصقاً (Sticker) يعبر عن حالتك الآن.",
                "ممنوع من استخدام ايموجيات في رسائلك لمدة 15 دقيقة.",
                "قل اسمك الحقيقي أو اسم مستعار مضحك في الشات.",
                "قم بعمل منشن لآخر شخص أرسل رسالة وقل له 'أنت تحت مراقبتي'.",
                "قل بصوت عالي في رسالة صوتية 'تحيا الإمبراطورية' 3 مرات."
            ];

            const selectedJudgment = judgments[Math.floor(Math.random() * judgments.length)];
            const victimId = victim.split('@')[0];

            const response = `
*⎔┄┄─ ⊱╎⌯ 𝐒 𝐎 𝐋 𝐎 ⌯╎⊰─┄┄⎔*
*⚖️┇ الـمـحـكـمـة الـإمـبـراطـوريـة ┇⚖️*
*⎔┄┄─── ⊱╎⌯ 🏛️ ⌯╎⊰ ───┄┄⎔*

*👤 الـمـحـكـوم عـلـيـه : ⦓ @${victimId} ⦔*

*📜 الـحـكـم الـنـهـائـي : ⦓ ${selectedJudgment} ⦔*

*⚠️ الـتـنـفـيذ فـوري.. لـا مـجـال لـلـاسـتـئـنـاف!*
*⎔┄┄── ⊱╎⌯ ⚖️ ⌯╎⊰ ──┄┄⎔*`.trim();

            await sock.sendMessage(chatId, { 
                text: response, 
                mentions: [victim] 
            }, { quoted: m });

        } catch (e) {
            console.error("Error in [احكم] command:", e);
        }
    }
};